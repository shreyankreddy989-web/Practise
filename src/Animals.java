class Animals {


    String name;
    String species;
    String breed;
    String color;
    String habitat;
    String country;
    String foodType;
    String sound;


    int age;
    int legs;
    int weight;
    int height;
    int speed;
    int lifespan;


    double weightKg;
    double heightMeters;
    double averageLifeSpan;


    boolean isWild;
    boolean isDomestic;
    boolean canFly;
    boolean canSwim;
    boolean carnivore;
    boolean mammal;

    public Animals(String name, String species, String breed, String color, String habitat, String country, String foodType, String sound, int age, int legs, int weight, int height, int speed, int lifespan, double weightKg, double heightMeters, double averageLifeSpan, boolean isWild, boolean isDomestic, boolean canFly, boolean canSwim, boolean carnivore, boolean mammal) {
        this.name = name;
        this.species = species;
        this.breed = breed;
        this.color = color;
        this.habitat = habitat;
        this.country = country;
        this.foodType = foodType;
        this.sound = sound;
        this.age = age;
        this.legs = legs;
        this.weight = weight;
        this.height = height;
        this.speed = speed;
        this.lifespan = lifespan;
        this.weightKg = weightKg;
        this.heightMeters = heightMeters;
        this.averageLifeSpan = averageLifeSpan;
        this.isWild = isWild;
        this.isDomestic = isDomestic;
        this.canFly = canFly;
        this.canSwim = canSwim;
        this.carnivore = carnivore;
        this.mammal = mammal;
    }
}


